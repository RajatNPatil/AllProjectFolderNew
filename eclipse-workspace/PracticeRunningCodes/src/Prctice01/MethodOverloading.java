package Prctice01;

public class MethodOverloading {
	
	    public int add(int a, int b) {
	        return a + b;
	    }

	    public double add(double a, double b) { // Overloaded method
	        return a + b;
	    }

	    public int add(int a, int b, int c) { // Overloaded method
	        return a + b + c;
	    }
	}


